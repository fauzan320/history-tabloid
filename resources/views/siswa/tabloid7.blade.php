                          <p style="margin-bottom: 0rem;text-indent: 0" class="description">
                          Hasil awal Perundingan <i>Renville</i> masih belum menemui titik terang, sehingga perundingan tersebut dilanjutkan di Kaliurang pada tanggal 14-15 Januari 1948 antara KTN dan RI. Tujuannya adalah membahas ultimatum Belanda pada tanggal 9 Januari 1948 yang mengharuskan RI menerima tuntutan mereka. Tuntutannya adalah agar RI menjawab penerimaan atau penolakan garis Van Mook sebagai garis demarkasi. Jika Indonesia tidak memenuhi tuntutan tersebut maka perundingan akan berakhir. 
